package com.cary.cwish.pojo;

public class RetireProcessPush {
	private String processPoint;
	
	private String operator;
	
	private String units;
	
	private String leaseNumber;
	
	private String startRetireTime;
	
	private String originalStatus;
	
	private String originalPassTime;

	private String email;
	
	public String getProcessPoint() {
		return processPoint;
	}

	public void setProcessPoint(String processPoint) {
		this.processPoint = processPoint;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

	public String getUnits() {
		return units;
	}

	public void setUnits(String units) {
		this.units = units;
	}

	public String getLeaseNumber() {
		return leaseNumber;
	}

	public void setLeaseNumber(String leaseNumber) {
		this.leaseNumber = leaseNumber;
	}

	public String getStartRetireTime() {
		return startRetireTime;
	}

	public void setStartRetireTime(String startRetireTime) {
		this.startRetireTime = startRetireTime;
	}

	public String getOriginalStatus() {
		return originalStatus;
	}

	public void setOriginalStatus(String originalStatus) {
		this.originalStatus = originalStatus;
	}

	public String getOriginalPassTime() {
		return originalPassTime;
	}

	public void setOriginalPassTime(String originalPassTime) {
		this.originalPassTime = originalPassTime;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
